    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        //BORRAR;
        int row = jtArticulos.getSelectedRow();
        
        if (row != -1) {
           int respuesta = JOptionPane.showConfirmDialog(this, "Seguro que desea borrar la fila " + row + "?","Confirmacion",
                    JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
           
            if (respuesta == JOptionPane.YES_OPTION) {
                
                comercio.eliminarArticulo(row);
                DefaultTableModel model = (DefaultTableModel)jtArticulos.getModel();
                model.removeRow(row);
            }
            
        }
        
    }//GEN-LAST:event_btnBorrarActionPerformed

