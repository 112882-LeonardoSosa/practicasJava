    private void cargarTabla(){
        DefaultTableModel modelo = new DefaultTableModel();
        
//        modelo.setColumnIdentifiers(new String[] {"Nombre","Codigo"});
        modelo.setColumnIdentifiers(new Object[]{"Codigo","Nombre","Precio","Stock"});
        
        
        
        for (Articulo a : comercio.obtenerTodos()) {
            modelo.addRow(new Object[]{a.getNombre(),a.getCodigo(),a.getPrecio(),a.getStock()});
        }
        
        jtArticulos.setModel(modelo);
    }
    
